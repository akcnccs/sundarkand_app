  @override
  void onInit() {
    super.onInit();
    setupAudio();
    lyricController.loadLyric(lyrics);
  }

  void setupAudio() async {
    try {
      await audioPlayer.setAsset('assets/mp3/hanuman_chalisha.mp3');

      audioPlayer.durationStream.listen((d) {
        if (d != null) duration.value = d;
      });

      audioPlayer.positionStream.listen((p) {
        position.value = p;
        lyricController.setProgress(p);
      });

      audioPlayer.playingStream.listen((playing) {
        isPlaying.value = playing;
      });
    } catch (e) {
      // Handle error
    }
  }

  void playPause() {
    if (audioPlayer.playing) {
      audioPlayer.pause();
    } else {
      audioPlayer.play();
    }
  }

  void seek(Duration pos) {
    audioPlayer.seek(pos);
  }

  @override
  void onClose() {
    audioPlayer.dispose();
    lyricController.dispose();
    super.onClose();
  }
